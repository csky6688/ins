package icom.system.service;

import javax.servlet.http.HttpServletRequest;

/**
 * 提供给客户端任务查询接口
 * 
 * @author huliubing
 * @since 2014-07-23
 *
 */
public interface TaskInterfaceService {

	/**
	 * 获取任务列表
	 * 
	 * @param jsonStr
	 * @return
	 */
	public String getAllTask(String jsonStr);
	
	/**
	 * 根据任务获取检查项列表
	 * 
	 * @param jsonStr
	 * @return
	 */
	public String getInspItems(String jsonStr);
	
	
	/**
	 * 问题工单保存
	 * 
	 * @param jsonStr
	 * @return
	 */
	public String saveTroubles(String jsonStr);
	
	
	/**
	 * 上传检查图片
	 * 
	 * @param request
	 * @return result
	 */
	public String uploadPhoto(HttpServletRequest request);
}
